    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $categories = Category::all();
        $public = 1;
        $public_articles = Article::where('public', $public);
        $articles = $public_articles->orderBy('created_at', 'desc')->take(3)->get();
        return view('categories.Index', ['categories' => $categories, 'articles' => $articles]);
    }

